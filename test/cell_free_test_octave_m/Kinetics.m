function rV = Kinetics(x,t,DF)

	% Grab the parameter vector from the data file struct -
	k = DF.KINETIC_PARAMETER_VECTOR;

	% Alias the species -
	A = x(1,1);
	ATP = x(2,1);
	B = x(3,1);
	NADH = x(4,1);
	C = x(5,1);
	F = x(6,1);
	G = x(7,1);
	D = x(8,1);
	E = x(9,1);
	H = x(10,1);
	O2 = x(11,1);
	ISOPRENE = x(12,1);
	ENZYME = x(13,1);
	X_S1 = x(14,1);
	X_S2 = x(15,1);
	X_F = x(16,1);
	X_D = x(17,1);
	X_E = x(18,1);
	X_H = x(19,1);
	X_O2 = x(20,1);
	X_ENZYME = x(21,1);
	X_ISOPRENE = x(22,1);
	X_BIOMASS = x(23,1);
	E_RXN_R1 = x(24,1);
	E_RXN_R2A = x(25,1);
	E_RXN_R2B = x(26,1);
	E_RXN_R3 = x(27,1);
	E_RXN_R4 = x(28,1);
	E_RXN_R5A = x(29,1);
	E_RXN_R6 = x(30,1);
	E_RXN_R7 = x(31,1);
	E_RXN_R8A = x(32,1);
	E_RXN_R8B = x(33,1);
	E_RXN_RES = x(34,1);
	E_RXN_ISOPRENE = x(35,1);
	E_RXN_TC1 = x(36,1);
	E_RXN_TC2 = x(37,1);
	E_RXN_TF = x(38,1);
	E_RXN_TD = x(39,1);
	E_RXN_TE = x(40,1);
	E_RXN_TH = x(41,1);
	E_RXN_TO2 = x(42,1);
	E_RXN_ISO_EXPORT = x(43,1);
	E_RXN_ENZ_EXPORT = x(44,1);
	E_RXN_GROWTH = x(45,1);
	E_RXN_ENZYME = x(46,1);

	% Reaction functions -
	rV = zeros(23,1);
	rV(1,1) = k(1,1)*E_RXN_R1*(A/(k(2,1)+A))*(ATP/(k(3,1)+ATP));
	rV(2,1) = k(4,1)*E_RXN_R2A*(B/(k(5,1)+B));
	rV(3,1) = k(6,1)*E_RXN_R2B*(C/(k(7,1)+C))*(ATP/(k(8,1)+ATP))*(NADH/(k(9,1)+NADH));
	rV(4,1) = k(10,1)*E_RXN_R3*(B/(k(11,1)+B));
	rV(5,1) = k(12,1)*E_RXN_R4*(C/(k(13,1)+C));
	rV(6,1) = k(14,1)*E_RXN_R5A*(G/(k(15,1)+G));
	rV(7,1) = k(16,1)*E_RXN_R6*(C/(k(17,1)+C));
	rV(8,1) = k(18,1)*E_RXN_R7*(C/(k(19,1)+C))*(NADH/(k(20,1)+NADH));
	rV(9,1) = k(21,1)*E_RXN_R8A*(G/(k(22,1)+G))*(ATP/(k(23,1)+ATP))*(NADH/(k(24,1)+NADH));
	rV(10,1) = k(25,1)*E_RXN_R8B*(H/(k(26,1)+H));
	rV(11,1) = k(27,1)*E_RXN_RES*(NADH/(k(28,1)+NADH))*(O2/(k(29,1)+O2));
	rV(12,1) = k(30,1)*E_RXN_ISOPRENE*(B/(k(31,1)+B))*(C/(k(32,1)+C))*(NADH/(k(33,1)+NADH))*(ATP/(k(34,1)+ATP));
	rV(13,1) = k(35,1)*E_RXN_TC1*(X_S1/(k(36,1)+X_S1));
	rV(14,1) = k(37,1)*E_RXN_TC2*(X_S2/(k(38,1)+X_S2));
	rV(15,1) = k(39,1)*E_RXN_TF*(X_F/(k(40,1)+X_F));
	rV(16,1) = k(41,1)*E_RXN_TD*(D/(k(42,1)+D));
	rV(17,1) = k(43,1)*E_RXN_TE*(E/(k(44,1)+E));
	rV(18,1) = k(45,1)*E_RXN_TH*(X_H/(k(46,1)+X_H));
	rV(19,1) = k(47,1)*E_RXN_TO2*(X_O2/(k(48,1)+X_O2));
	rV(20,1) = k(49,1)*E_RXN_ISO_EXPORT*(ISOPRENE/(k(50,1)+ISOPRENE));
	rV(21,1) = k(51,1)*E_RXN_ENZ_EXPORT*(ENZYME/(k(52,1)+ENZYME));
	rV(22,1) = k(53,1)*E_RXN_GROWTH*(C/(k(54,1)+C))*(F/(k(55,1)+F))*(H/(k(56,1)+H))*(ATP/(k(57,1)+ATP));
	rV(23,1) = k(58,1)*E_RXN_ENZYME*(C/(k(59,1)+C))*(F/(k(60,1)+F))*(H/(k(61,1)+H))*(ATP/(k(62,1)+ATP));
return
